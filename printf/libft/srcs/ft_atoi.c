int	ft_atoi(const char *str)
{
	int	i;
	int	j;
	int	k;

	i = 0;
	j = 1;
	k = 0;
	while (str[i] == 32 || str[i] == '\t')
		i++;
	if (str[i] == 43 || str[i] == 45)
	{
		if (str[i] == 45)
			j = -1;
		i++;
	}
	while (str[i] >= 48 && str[i] <= 57)
	{
		k *= 10;
		k += str[i] - 48;
		i++;
	}
	return (k * j);
}
