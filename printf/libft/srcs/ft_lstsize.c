int	ft_lstsize(t_list *lst)
{
	int		i;
	t_list	*z;

	i = 0;
	z = lst;
	while (z->next)
	{
		i++;
		z = z->next;
	}
	if (z->next == 0)
		i++;
	return (i);
}
